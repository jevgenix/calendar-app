import type { NextPage } from 'next'
import Head from 'next/head'
import { useSession, signOut, signIn, getSession, GetSessionParams } from 'next-auth/react'
import styles from '../styles/Home.module.css'

const Home: NextPage = () => {
    const { data: session, status } = useSession();
    console.log(session)
    if (status === 'authenticated') {
        return (
            //TÄHÄN DESIGN
            <div className={styles.container}>
                <Head>
                    <title>Home page</title>
                    <h1>Welcome to the home page, {session?.user?.name}!</h1>
                    <meta name="description" content="Generated by create next app" />
                    <link rel="icon" href="/favicon.ico" />
                </Head>
                <div>
                    <button onClick={() => signOut()}>Sign out</button>
                </div>
            </div>
        )
    } else {
        return (
            //TÄHÄN MYÖS
            <div>
                <p>You are not signed in.</p>
                <button onClick={() => signIn()}>Sign in</button>
            </div>
        )
    }
}

export default Home

export const getServerSideProps = async (context: GetSessionParams | undefined) => {
    const session = await getSession(context)

    if (!session) {
        return {
            redirect: {
                destination: '/login'
            }
        }
    }
    return {
        props: { session }
    }
}