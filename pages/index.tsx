import Head from "next/head";
import styles from "../styles/Home.module.css";
import type { GetStaticProps, NextPage } from "next";
import { Schedule, GetSResults } from "../types";

const Home: NextPage<{ tasks: Schedule[] }> = ({ tasks }) => {
  return (
    <div className={styles.container}>
      <Head>
        <title>Home page</title>
        <h1>Welcome to the home page!</h1>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main className={styles.main}>
        <div className={styles.calendar}>
          {tasks.map((task) => {
            return (
              <ul key={task.id}>
                <li>{task.task1}</li>
                <li>{task.task2}</li>
                <li>{task.task3}</li>
              </ul>
            );
          })}
        </div>
      </main>
    </div>
  );
};

export const getStaticProps: GetStaticProps = async (context) => {
  //haetaan dummy json dataa
  const res = await fetch(
    "https://raw.githubusercontent.com/vhjaho/calendar/main/calendar.json"
  );
  //resultit tallennetaan arrayhyn
  const { results }: GetSResults = await res.json();

  return {
    //apin lähettämät propsit nimetään = tasks
    props: {
      tasks: results,
    },
  };
};

export default Home;
